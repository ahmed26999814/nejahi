create table if not exists public.result_alert_subscriptions (
  id bigint generated by default as identity primary key,
  exam_slug text not null default 'bac-2026',
  full_name text not null,
  whatsapp text not null,
  whatsapp_normalized text not null,
  notified boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  notified_at timestamptz,
  constraint result_alert_subscriptions_name_length check (char_length(btrim(full_name)) between 2 and 100),
  constraint result_alert_subscriptions_phone_length check (char_length(whatsapp_normalized) between 8 and 15),
  constraint result_alert_subscriptions_exam_phone_unique unique (exam_slug, whatsapp_normalized)
);

create index if not exists result_alert_subscriptions_created_at_idx
  on public.result_alert_subscriptions (created_at desc);

create index if not exists result_alert_subscriptions_notified_idx
  on public.result_alert_subscriptions (notified, created_at desc);

alter table public.result_alert_subscriptions enable row level security;

revoke all on table public.result_alert_subscriptions from anon, authenticated;
grant select, insert, update, delete on table public.result_alert_subscriptions to service_role;
grant usage, select on sequence public.result_alert_subscriptions_id_seq to service_role;

comment on table public.result_alert_subscriptions is
  'WhatsApp alert subscriptions for upcoming national examination results.';
